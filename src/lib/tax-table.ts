/**
 * 간이세액표에서 소득세 조회
 * @param taxableIncome 과세 소득 (월급여 과세분 + 초과수당)
 * @param dependents 부양가족 수 (본인 포함)
 * @param taxRate 원천세율 (보통 100, 비거주자 등 120)
 * @returns 소득세
 */
export function lookupIncomeTax(
  taxableIncome: number,
  dependents: number,
  taxRate: number = 100
): number {
  // 부양가족 인덱스 (1~11, 11초과시 11로)
  const depIdx = Math.min(Math.max(dependents, 1), 11) - 1;

  // 1400만원 초과: 마지막 구간 세액 + 초과분의 45%
  if (taxableIncome >= 14000000) {
    const baseTax = TAX_TABLE[TAX_TABLE.length - 1][2][depIdx];
    const excess = taxableIncome - 14000000;
    const tax = baseTax + Math.round(excess * 0.45);
    return Math.round(tax * taxRate / 100);
  }

  // 구간 찾기
  for (const [lower, upper, taxes] of TAX_TABLE) {
    if (taxableIncome >= lower && taxableIncome < upper) {
      const tax = taxes[depIdx];
      return Math.round(tax * taxRate / 100);
    }
  }

  return 0;
}

/**
 * 주민세 계산 (소득세의 10%, 10원 미만 절사)
 */
export function calculateResidentTax(incomeTax: number): number {
  return Math.floor(incomeTax * 0.1 / 10) * 10;
}

/**
 * 부양가족 문자열 파싱 ("4(2)" → 4, "3" → 3)
 */
export function parseDependents(str: string | null): number {
  if (!str) return 1;
  const match = String(str).match(/^(\d+)/);
  return match ? parseInt(match[1]) : 1;
}
